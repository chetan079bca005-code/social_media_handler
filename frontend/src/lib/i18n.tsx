// ============ Context ============
interface I18nContextType {
  language: LanguageCode
  setLanguage: (lang: LanguageCode) => void
  t: (key: keyof TranslationKeys) => string
  dir: 'ltr' | 'rtl'
}

const I18nContext = createContext<I18nContextType>({
  language: 'en',
  setLanguage: () => {},
  t: (key) => key,
  dir: 'ltr',
})

// ============ Provider ============
export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<LanguageCode>(() => {
    const saved = localStorage.getItem('app_language')
    if (saved && translations[saved as LanguageCode]) {
      return saved as LanguageCode
    }
    // Auto-detect browser language
    const browserLang = navigator.language.split('-')[0]
    if (translations[browserLang as LanguageCode]) {
      return browserLang as LanguageCode
    }
    return 'en'
  })

  const setLanguage = useCallback((lang: LanguageCode) => {
    setLanguageState(lang)
    localStorage.setItem('app_language', lang)
    // Set document direction for RTL languages
    const langConfig = SUPPORTED_LANGUAGES.find(l => l.code === lang)
    document.documentElement.dir = langConfig?.dir || 'ltr'
    document.documentElement.lang = lang
  }, [])

  // Initialize direction on mount
  useEffect(() => {
    const langConfig = SUPPORTED_LANGUAGES.find(l => l.code === language)
    document.documentElement.dir = langConfig?.dir || 'ltr'
    document.documentElement.lang = language
  }, [language])

  const t = useCallback((key: keyof TranslationKeys): string => {
    return translations[language]?.[key] || translations.en[key] || key
  }, [language])

  const dir = SUPPORTED_LANGUAGES.find(l => l.code === language)?.dir as 'ltr' | 'rtl' || 'ltr'

  return (
    <I18nContext.Provider value={{ language, setLanguage, t, dir }}>
      {children}
    </I18nContext.Provider>
  )
}

// ============ Hook ============
export function useI18n() {
  return useContext(I18nContext)
}

export default I18nContext
